package dmc.model;

public class Label {

    private String url;
    private String name;
    private String color;

    /**
     * Gets the url
     *
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * Sets the url
     *
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * Gets the name
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name
     *
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets the color
     *
     * @return the color
     */
    public String getColor() {
        return color;
    }

    /**
     * Sets the color
     *
     * @param color the color to set
     */
    public void setColor(String color) {
        this.color = color;
    }

}
